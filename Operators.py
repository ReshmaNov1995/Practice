a=10
b=2

# Arithmetic Operators
print(a+b)
print(a-b)
print(a*b)
print(a/b)

print(a//b) # Quotient
print(a%b) # Reminder
print(a**b) # Exponentiation 10**2 = 100

# Relational Operator
# It will return only Boolean Values
# < > <= >= != ==

a=10
b=30

print(a>b)
print(a<b)
print(a!=b)
print(a==b)
print(a<=b)
print(a>=b)

# Logical Operator
# and or not

# Note: Boolean values should represent caps as first letter. Ex: True False

a = True
b = False

print(a and b)
print(a or b)
print(not a)
